package com.walking.lesson1_varaibles;

/**
 * Используя одну переменную типа char вывести свое имя в консоль.
 */
public class Task2SingleChar {
    public static void main(String[] args) {
        char ch = 'J';
        System.out.print(ch);

        ch = 'a';
        System.out.print(ch);

        ch = 'c';
        System.out.print(ch);

        ch = 'k';
        System.out.print(ch);
    }
}
